// (desafio I) Faça um programa que imprima na tela o códico ASCII das letras de ’A’ a ’D’.

#include <stdio.h>

int main(void)
{
    // imprimindo valor ASCII das letras
    printf("Valor ASCII da letra %c: %d\n", 'A', 'A');
    printf("Valor ASCII da letra %c: %d\n", 'B', 'B');
    printf("Valor ASCII da letra %c: %d\n", 'C', 'C');
    printf("Valor ASCII da letra %c: %d\n", 'D', 'D');

    return 0;  
}